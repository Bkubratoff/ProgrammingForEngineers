# Métodos de listas
# otros metodos utilizados son append, remove y reverse
#
# append(), añade un elemento donde la lista es llamada
# remove(), remueve un elemento que es validado en la entrada
# reverse(), reversa una lista

# creamos una lista de experimentos
valores = [10.2, 29.0, 20.2, 29.0, 87.2]

# agregamos otros valores
valores.append(20.2)
valores.append(5.1)

# imprimimos los valores
print(valores)

# reversamos el orden de elementos
valores.reverse()

# imprimimos los valores
print(valores)